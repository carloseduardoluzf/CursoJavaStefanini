package com.stefanini.aula3variaveis;

public class Variaveis {

	public static void main(String[] args) {
		byte numByte = -128; //-128 a 127 (8bits)
		short numShort = 12321; // -32768 a 32767 (16bits)
		int numInt = 2147483647; // -2.147.483.648.647 (32bits)
		long numLong = 2147483647; // -9.223.372.036.854.770.000 a 9.223.372.036.854.770.000 (64bits)
		
		float numFloat = 10.000f; // -1,4024E-37 a 3.40282347E+308 (64bits)
		double numDouble = 10000000000000.000; // -4,94E-307 a 1.79769313486231570E+308
		
		char varChar = 'F'; // 0 a 65535 (16bits)
		
		boolean varBoolean = true; // true or false (1bit)
		
		String texto = "Bem vindos a Stefanini";
		Integer numeroConta = 4000;
		Long numeroLongo = 1213123L;
		
		// Ele deve começar com uma Letra minúsculo. Exemplo: nome
		// Não deve começar com caracteres especiais como & (e comercial), $ (dolar) _ (sublinhado). Exemplo: _nome
		// Se o nome for composto por mais de uma palavra, inicie-se com a primeira palavra com a letra minúscula e em seguida na outra palavra letra maiúscula. Exemplo: nomeAluno;
		// Evite usar variáveis de um caractere. Exemplo: a;
		
		String nomeCompleto = "Davide";
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
